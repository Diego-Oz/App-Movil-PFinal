package com.example.tema9;

public class AlimentosTotal {

    private int id;
    private String nombret;
    private String descripciont;
    private String preciot;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombret() {
        return nombret;
    }

    public void setNombret(String nombret) {
        this.nombret = nombret;
    }

    public String getDescripciont() {
        return descripciont;
    }

    public void setDescripciont(String descripciont) {
        this.descripciont = descripciont;
    }

    public String getPreciot() {
        return preciot;
    }

    public void setPreciot(String preciot) {
        this.preciot = preciot;
    }

}
